def draw_char(superficie, x, y, ch, cor, escala=3, setPixel=None):
    if ch not in FONTE or setPixel is None:
        return

    mapa = FONTE[ch]
    for j, linha in enumerate(mapa):
        for i, bit in enumerate(linha):
            if bit == "1":
                for dx in range(escala):
                    for dy in range(escala):
                        setPixel(superficie,
                                 x + i * escala + dx,
                                 y + j * escala + dy,
                                 cor)
                        
def draw_text(superficie, x, y, texto, cor, escala=3, esp=2, setPixel=None):
    px = x
    for ch in texto:
        if ch == " ":
            px += (5 * escala)
        else:
            draw_char(superficie, px, y, ch, cor, escala, setPixel)
            px += (5 + esp) * escala